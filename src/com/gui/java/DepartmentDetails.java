    private void searchbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchbtnActionPerformed
         String dept_code = txtdept_code.getText();
        
       
          String update = "select * from departments where dept_code = ?";
            try {
                Connection con = DBConnection1.getDBConnection1();
                PreparedStatement psmt = con.prepareStatement(update);
                psmt.setString(1, dept_code);
                ResultSet rs = psmt.executeQuery();
              
                if(rs.next()){
              
                txtdeptcode.setText(rs.getString("dept_code"));
                txtname.setText(rs.getString("name"));
                txthead.setText(rs.getString("head"));
                txtlocation.setSelectedItem(rs.getString("location"));
               
                }else{
                    JOptionPane.showMessageDialog(null, "Department code does not match");
                }
                setVisible(true);
            } catch (Exception e) {
                System.out.println(e);
            }
  
    
    }//GEN-LAST:event_searchbtnActionPerformed

