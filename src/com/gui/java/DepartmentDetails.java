    private void addbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addbtnActionPerformed
       String dept_code = txtdeptcode.getText();
       String name = txtname.getText();
       String head = txthead.getText();
       String location = txtlocation.getSelectedItem().toString();
       
       String sql = "insert into departments(dept_code,name,head,location) values(?,?,?,?)";
       
       try {
           Connection con = DBConnection1.getDBConnection1();
           PreparedStatement  stmt = con.prepareStatement(sql);
            stmt.setString(1,dept_code);
            stmt.setString(2, name);
            stmt.setString(3, head);
            stmt.setString(4, location);
           
            int status = stmt.executeUpdate();
            JOptionPane.showMessageDialog(null, "Department Added Successful!");
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "This code is already used!");
            System.out.println(e);
        }
    }//GEN-LAST:event_addbtnActionPerformed

